const mongoose = require('mongoose');

const accessTokenSchema = mongoose.Schema({
    userId: {
        type: String,
        required: true
    },
    clientId: {
        type: String,
        required: true
    },
    token: {
        type: String,
        unique: true,
        required: true
    },
    created: {
        type: Date,
        default: Date.now
    }
});

module.exports.AccessTokenModel = mongoose.model('AccessToken', accessTokenSchema);
